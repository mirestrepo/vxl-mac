// This is vxl/vcsl/vcsl_degree.cxx
#include "vcsl_degree.h"

#include <vnl/vnl_math.h>

//***************************************************************************
// Constructors/Destructor
//***************************************************************************

//---------------------------------------------------------------------------
// Destructor
//---------------------------------------------------------------------------
vcsl_degree::~vcsl_degree()
{
}

//***************************************************************************
// Status report
//***************************************************************************

//---------------------------------------------------------------------------
// Returns the number of units of `this' equal of the standard_unit for
// the dimension
//---------------------------------------------------------------------------
double vcsl_degree::units_per_standard_unit(void) const
{
  return 180/vnl_math::pi;
}

//***************************************************************************
// Singleton pattern
//***************************************************************************

//---------------------------------------------------------------------------
// Return the reference to the unique vcsl_degree object
//---------------------------------------------------------------------------
vcsl_degree_sptr vcsl_degree::instance(void)
{
  if (instance_.ptr()==0)
    instance_=new vcsl_degree;
  return instance_;
}

//---------------------------------------------------------------------------
// Default constructor
//---------------------------------------------------------------------------
vcsl_degree::vcsl_degree(void)
{
}

//---------------------------------------------------------------------------
// Reference to the unique vcsl_degree object
//---------------------------------------------------------------------------
vcsl_degree_sptr vcsl_degree::instance_=0;
