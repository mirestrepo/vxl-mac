#include <vcsl/vcsl_meter.h>

//***************************************************************************
// Constructors/Destructor
//***************************************************************************

//---------------------------------------------------------------------------
// Destructor
//---------------------------------------------------------------------------
vcsl_meter::~vcsl_meter()
{
}

//***************************************************************************
// Status report
//***************************************************************************

//---------------------------------------------------------------------------
// Returns the number of units of `this' equal of the standard_unit for
// the dimension
//---------------------------------------------------------------------------
double vcsl_meter::units_per_standard_unit(void) const
{
  return 1;
}

//***************************************************************************
// Singleton pattern
//***************************************************************************

//---------------------------------------------------------------------------
// Return the reference to the unique vcsl_meter object
//---------------------------------------------------------------------------
vcsl_meter_ref vcsl_meter::instance(void)
{
  if(_instance.ptr()==0)
    _instance=new vcsl_meter;
  return _instance;
}

//---------------------------------------------------------------------------
// Default constructor
//---------------------------------------------------------------------------
vcsl_meter::vcsl_meter(void)
{
}

//---------------------------------------------------------------------------
// Reference to the unique vcsl_meter object
//---------------------------------------------------------------------------
vcsl_meter_ref vcsl_meter::_instance=0;
